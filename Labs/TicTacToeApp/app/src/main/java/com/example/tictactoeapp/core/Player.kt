package com.example.tictactoeapp.core

/**
 * Базовый игрок в игре крестики-нолики.
 * Может быть человеком или компьютером.
 */
interface Player {

    /**
     * Символ игрока ('X' или 'O').
     */
    val symbol: Char

    /**
     * Имя игрока (для авторизации и таблицы лидеров).
     */
    val name: String

    /**
     * Делает ход.
     * Для человека — координаты передаются извне.
     * Для компьютера — координаты выбираются автоматически.
     *
     * @param board текущее состояние поля
     * @return пара (row, col) — выбранная клетка
     */
    fun makeMove(board: GameBoard): Pair<Int, Int>
}
